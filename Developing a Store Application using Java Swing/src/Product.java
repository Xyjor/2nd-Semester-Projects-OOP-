public class Product {
    private int id;
    private String Name;
    private double price;

    public Product(int id, String Name, double price) {
        this.id = id;
        this.Name = Name;
        this.price = price;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return Name;
    }

    public double getPrice() {
        return price;
    }

  
    
}
